public class VariablesinJavaPrograms {

	public static void main(String[] args) {
		double l;
		double w;
		double a;
		double p;
		double c;
		double ac;
		double r;
		
		l = 18;
		w = 26;
		r = 24;
		
		a = l*w;
		p = 2*l + 2*w;
		c = 2 * 3.14 * r;
		ac = 3.14*r*r;
		
		System.out.println("Area: " + a);
		System.out.println("Perimeter: " + p);
		System.out.println("Circumference: " + c);
		System.out.println("Area of Circle: " + ac);

	}

}
